function getRandomColor() {
  // Simple random color generator for chart lines
  const letters = '0123456789ABCDEF';
  let color = '#';
  for (let i = 0; i < 6; i++) {
    color += letters[Math.floor(Math.random() * 16)];
  }
  return color;
}

function calculateLinearRegression(labels, values) {
  const n = values.length;
  const xVals = labels.map((_, i) => i);
  const yVals = values;

  const sumX = xVals.reduce((a, b) => a + b, 0);
  const sumY = yVals.reduce((a, b) => a + b, 0);
  const sumXY = xVals.reduce((sum, x, i) => sum + x * yVals[i], 0);
  const sumX2 = xVals.reduce((sum, x) => sum + x * x, 0);

  const slope = (n * sumXY - sumX * sumY) / (n * sumX2 - sumX * sumX);
  const intercept = (sumY - slope * sumX) / n;

  const trendline = xVals.map(x => slope * x + intercept);
  return trendline;
}

function detectOutliers(values, threshold = 2) {
  const mean = values.reduce((a, b) => a + b, 0) / values.length;
  const stdDev = Math.sqrt(values.map(v => Math.pow(v - mean, 2)).reduce((a, b) => a + b, 0) / values.length);

  return values.map(v => {
    const isOutlier = Math.abs(v - mean) > threshold * stdDev;
    return { value: v, isOutlier };
  });
}

