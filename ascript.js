function summarizeDataPoints(dataPoints, timeRange = "Daily") {
  if (!Array.isArray(dataPoints)) {
    console.error("dataPoints is not an array:", dataPoints);
    return { timeRange, labels: [], values: [] };
  }

  // Check for bad entries and log them
  for (const point of dataPoints) {
    if (!point || typeof point !== 'object') {
      console.warn("Bad point found:", point);
      continue;
    }
    if (!point.time || isNaN(new Date(point.time))) {
      console.warn("Bad point found (invalid time):", point);
      continue;
    }
    if (typeof point.value !== 'number' || isNaN(point.value)) {
      console.warn("Bad point found (invalid value):", point);
      continue;
    }

    if (!point.time || isNaN(new Date(point.time))) {
      console.warn("Bad point found:", point);
    }
  }

  const days = ['Sun', 'Mon', 'Tue', 'Wed', 'Thu', 'Fri', 'Sat'];
  const months = ['Jan', 'Feb', 'Mar', 'Apr', 'May', 'Jun', 'Jul', 'Aug', 'Sep', 'Oct', 'Nov', 'Dec'];

  const groups = {};         // label ‚Üí values[]
  const labelSortKey = {};   // label ‚Üí sortKey for ordering
  const labelBaseDate = {};  // label ‚Üí Date object (for display)

  for (const { time, value } of dataPoints) {
    if (!time || isNaN(new Date(time))) {
      console.warn("Bad point found:", { time, value });
      continue;
    }

    const date = new Date(time);
    if (isNaN(date.getTime())) {
      console.warn("Invalid time value encountered:", time);
      continue; // Skip this entry
    }
  
    let label;
    let sortKey;
  
    if (timeRange === "Daily") {
      const dayName = days[date.getUTCDay()];
      const mmdd = `${String(date.getUTCMonth() + 1).padStart(2, '0')}/${String(date.getUTCDate()).padStart(2, '0')}`;
      label = `${dayName} [${mmdd}]`;
      sortKey = date.toISOString();
    } else if (timeRange === "Weekly") {
      const weekNum = getISOWeekNumber(date);
      label = `W${weekNum}`;
      sortKey = `${date.getUTCFullYear()}-${weekNum.toString().padStart(2, '0')}`;
    } else if (timeRange === "Monthly") {
      const month = date.getUTCMonth();
      label = months[month];
      sortKey = `${date.getUTCFullYear()}-${month.toString().padStart(2, '0')}`;
    } else {
      throw new Error("Invalid timeRange: choose 'Daily', 'Weekly', or 'Monthly'");
    }
  
    if (!groups[label]) {
      groups[label] = [];
      labelSortKey[label] = sortKey;
      labelBaseDate[label] = date;
    }
  
    groups[label].push(value);
  }

  const sortedLabels = Object.keys(groups).sort(
    (a, b) => labelSortKey[a].localeCompare(labelSortKey[b])
  );

  const values = sortedLabels.map(label => average(groups[label]));

  return {
    timeRange,
    labels: sortedLabels,
    values,
    mean: average(values)
  };

  function average(arr) {
    if (arr.length === 0) return 0;
    return arr.reduce((a, b) => a + b, 0) / arr.length;
  }

  function getISOWeekNumber(date) {
    const tmp = new Date(Date.UTC(date.getUTCFullYear(), date.getUTCMonth(), date.getUTCDate()));
    const dayNum = tmp.getUTCDay() || 7;
    tmp.setUTCDate(tmp.getUTCDate() + 4 - dayNum);
    const yearStart = new Date(Date.UTC(tmp.getUTCFullYear(), 0, 1));
    return Math.ceil((((tmp - yearStart) / 86400000) + 1) / 7);
  }
}

