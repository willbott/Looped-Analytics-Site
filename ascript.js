document.getElementById('compareBtn').addEventListener('click', () => {
    const game1 = document.getElementById('compareGame1').value;
    const game2 = document.getElementById('compareGame2').value;
    const category = document.getElementById('compareCategory').value;
    const timeRange = document.getElementById('compareTimeRange').value;
  
    if (!game1 || !game2 || !category || !timeRange) {
      alert('Please select both games, category, and time range.');
      return;
    }
  
    if (game1 === game2) {
      alert('Please select two different games.');
      return;
    }
  
    const data1 = (games.find(g => g.name === game1)?.data || []).find(d => d.name === category);
    const data2 = (games.find(g => g.name === game2)?.data || []).find(d => d.name === category);
  
    if (!data1 || !data2) {
      alert('Selected category not found in one or both games.');
      return;
    }
  
    const summary1 = summarizeDataPoints(data1.data, timeRange);
    const summary2 = summarizeDataPoints(data2.data, timeRange);
  
    const allLabelsSet = new Set([...summary1.labels, ...summary2.labels]);
    const allLabels = Array.from(allLabelsSet).sort();
  
    function alignValues(summary, labels) {
      const map = new Map(summary.labels.map((label, i) => [label, summary.values[i]]));
      return labels.map(label => map.has(label) ? map.get(label) : 0);
    }
  
    const alignedValues1 = alignValues(summary1, allLabels);
    const alignedValues2 = alignValues(summary2, allLabels);
  
    if (comparisonChart) {
      comparisonChart.destroy();
      comparisonChart = null;
    }
  
    const ctx = document.getElementById('comparisonChart').getContext('2d');
    comparisonChart = new Chart(ctx, {
      type: 'line',
      data: {
        labels: allLabels,
        datasets: [
          {
            label: `${game1} - ${category}`,
            data: alignedValues1,
            borderColor: 'rgba(75, 192, 192, 1)',
            backgroundColor: 'rgba(75, 192, 192, 0.2)',
            fill: false,
            tension: 0.3,
          },
          {
            label: `${game2} - ${category}`,
            data: alignedValues2,
            borderColor: 'rgba(255, 99, 132, 1)',
            backgroundColor: 'rgba(255, 99, 132, 0.2)',
            fill: false,
            tension: 0.3,
          }
        ]
      },
      options: {
        responsive: true,
        plugins: {
          legend: { position: 'top' },
          title: {
            display: true,
            text: `Comparison of ${category} (${timeRange})`
          }
        },
        scales: {
          y: { beginAtZero: true }
        }
      }
    });
});

